package com.ca.asynchmsg.message;

public class MessageUID {
	
	private String messageUID;
	
	private String maskedMessageUID;
	
	public String getUID() {
		return messageUID;
	}
	public void setUID(String messageUID) {
		this.messageUID = messageUID;
	}
	
	public String getMaskedUID() {
		return maskedMessageUID;
	}
	public void setMaskedUID(String maskedMessageUID) {
		this.maskedMessageUID = maskedMessageUID;
	}
	
	
}
